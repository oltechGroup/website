// Gsap Imports
import gsap from "gsap";
import { ScrollTrigger } from "gsap/ScrollTrigger";

function Header({
  hidden = false,
  transparent = false,
  principal = false,
  animation = false,
}) {
  gsap.registerPlugin(ScrollTrigger);
  const [menuOpen, setMenuOpen] = useState(false);
  const nav = useNavigate();

  // If scroll is top, then hide box-shadow
  const [scrollTop, setScrollTop] = useState(true);
  const handleScroll = () => {
    if (window.scrollY > 0) {
      setScrollTop(false);
    } else {
      setScrollTop(true);
    }
  };

  useEffect(() => {
    window.addEventListener("scroll", handleScroll);

    return () => window.removeEventListener("scroll", handleScroll);
  }, []);

  const classes = [
    "nav-oltech",
    principal ? "principal" : "",
    hidden ? "hidden" : "",
    transparent ? "transparent" : "",
    scrollTop ? "transparent" : "shadow",
    menuOpen ? "open" : "",
    animation ? "animation" : "",
  ].join(" ");

  const renderLogo = () => {
    if (principal && !menuOpen) {
      return <LogoWhite />;
    } else {
      return <LogoColor />;
    }
  }
  
  return (
    <>
      <nav className={classes}>
        <section className="nav-oltech__container">
          <div className="items">
            <div onClick={() => nav(routes.home)}>
              {renderLogo()}
            </div>
            <ul>
              <li>
                <Link to={routes.home}>Inicio</Link>
              </li>
              <li>
                <Link to={routes.about}>Nosotros</Link>
              </li>
              <li>
                <Link to={routes.products}>Productos</Link>
              </li>
              <li>
                <Link to="#">Ayuda</Link>
              </li>
            </ul>
          </div>
          <button
            className="button-fill btn-contact"
            onClick={() => {
              nav(routes.contact);
              setMenuOpen(false);
            }}
          >
            Contáctanos
          </button>
          <div
            className={`btn-burger ${menuOpen ? "btn-burger--open" : ""} ${principal ? "btn-burger--white" : ""}`}
            onClick={() => setMenuOpen(!menuOpen)}
          >
            <span className="icon-burger">
              <i className="line"></i>
              <i className="line"></i>
              <i className="line"></i>
            </span>
          </div>
        </section>
      </nav>

      {menuOpen && (
        <section className="nav-oltech__menu">
          <button
            className="button-fill btn-contact"
            onClick={() => {
              nav(routes.contact);
              setMenuOpen(false);
            }}
          >
            Contáctanos
          </button>
          <ul>
            <li>
              <Link to={routes.home}>Inicio</Link>
            </li>
            <li>
              <Link to={routes.about}>Nosotros</Link>
            </li>
            <li>
              <Link to={routes.products}>Productos</Link>
            </li>
            <li>
              <Link to="#">Ayuda</Link>
            </li>
          </ul>
        </section>
      )}
    </>
  );
}

